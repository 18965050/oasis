package cn.xyz.chaos.jakarta.common.model;

public class Car {

    private int fuel;

    private int mile;

    public Car() {
        this.fuel = 5;
    }

    public int getFuel() {
        return fuel;
    }

    public void setFuel(int fuel) {
        this.fuel = fuel;
    }

    public int getMile() {
        return mile;
    }

    public void setMile(int mile) {
        this.mile = mile;
    }

}
